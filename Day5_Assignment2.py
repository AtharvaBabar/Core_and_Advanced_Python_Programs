#Q2. Declare a square() function with one parameter.Then call the function and pass one number and display the square of that number.

# Function to calculate square of a number
def square(num):
    return num * num  # Multiply the number by itself

# Calling the function with a number
result = square(5)

# Displaying the result
print("Square of the number:", result)


'''
OUTPUT:
Square of the number: 25
'''
